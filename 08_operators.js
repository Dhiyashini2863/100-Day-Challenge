// getting input from the users
const x =  parseInt(prompt('Enter a number: '));
const y =  parseInt(prompt('Enter another number: '));
// addition
console.log('x + y = ', x + y);

// subtraction
console.log('x - y = ', x - y);

// multiplication
console.log('x * y = ', x * y);

// division
console.log('x / y = ', x / y);

// remainder
console.log('x % y = ', x % y); 

//exponentiation
console.log('x ** y =', x ** y);
